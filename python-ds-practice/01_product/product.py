def product(a, b):
    """Return product of a and b.

    >>> product(2, 2)
    4

    >>> product(2, -2)
    -4
    """
    return a * b

# Example 1
result1 = product(2, 2)
print("Product of 2 and 2:", result1)  # Output: 4

# Example 2
result2 = product(2, -2)
print("Product of 2 and -2:", result2)  # Output: -4
